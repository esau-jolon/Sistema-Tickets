package Models;

/**
 *
 * @author esauj
 */
public class Empresa {

    private int id;
    private String nombre;
    private String logo;
    private String idioma;
    private String hora;
    private String tiempo_expiracion; // mismo nombre que en la BD

    // Getters y setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }



    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getTiempo_expiracion() {
        return tiempo_expiracion;
    }

    public void setTiempo_expiracion(String tiempo_expiracion) {
        this.tiempo_expiracion = tiempo_expiracion;
    }
}
